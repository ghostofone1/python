class ncolor:
    HEADER = '\033[95m' # pink
    BLUE = '\033[94m'  # blue
    WARNING = '\033[93m' # yellow
    GREEN = '\033[92m'  # green
    RED = '\033[91m'  # red
    ENDC = '\033[0m'


class nformat:
    BOLD = "\x1b[1m"
    DIM = "\x1b[2m"
    ITALIC = "\x1b[3m"
    UNDERLINE = "\x1b[4m"
    ENDF = "\x1b[0m"
    EBOLD = "\x1b[21m"
    EDIM = "\x1b[22m"
    EITALIC = "\x1b[23m"
    EUNDERLINE = "\x1b[24m"

